    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Botón borrar
        int resp = JOptionPane.showConfirmDialog(null, "¿Borrar Proveedor "+jTextField2.getText()+" ?", "Borrar", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if(resp==1)
            return;
            try {
                Conexiones.Conexion.BorrarProveedor(jTextField1.getText());
            } catch (SQLException ex) {}
            try{
                ResultSet rs=Conexiones.Conexion.Consulta("select count(NoProveedor) from Proveedor");
                while(rs.next())
                    if(contador>rs.getInt(1))
                        contador--;
            }catch(SQLException e){} 
            if(contador==0){
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jTextField6.setText("");
                jTextField7.setText("");
                jTextField8.setText("");
                jTextField9.setText("");
                jTextField10.setText("");
            }    
            else
                CargarPantalla();
    }//GEN-LAST:event_jButton3ActionPerformed

