    public void CargarProdductos(){
        String cont=String.valueOf(contador);
        DefaultTableModel modelo=(DefaultTableModel) jTable2.getModel();
                modelo.setRowCount(0);
                res1 = Conexiones.Conexion.Consulta("select Id,NombreProducto,Marca,ContNet,Unidad,Piezas,Departamento,Costo,PrecioPúblico,Existencias \n" +
                                                    "from Producto,ProvProducto \n" +
                                                    "where ProvProducto.Proveedor="+cont+" and Id="+cont+";");
                try{
                    while(res1.next()){
                        Vector v = new Vector();
                        v.add(res1.getInt(1));//Id
                        v.add(res1.getString(2));//NombreProducto
                        v.add(res1.getString(3));//Marca
                        v.add(res1.getFloat(4));//ContNet
                        v.add(res1.getString(5));//Unidad
                        v.add(res1.getInt(6));//Piezas
                        v.add(res1.getString(7));//Departamento
                        v.add(res1.getFloat(8));//Costo
                        v.add(res1.getFloat(9));//Precio
                        v.add(res1.getInt(10));//Existencias
                        modelo.addRow(v);
                        jTable2.setModel(modelo);
                    }
                }catch (SQLException e){}
    }
    
    
    public void CargarPantalla(){
        String numero=String.valueOf(contador);
        String SentenciaConsulta="select * from(select ROW_NUMBER() over(order by NoProveedor) as Row#, * from Proveedor) x where Row#="
                +numero+";";
        res=Conexiones.Conexion.Consulta(SentenciaConsulta);
        try{
            while(res.next()){
                int dato1=res.getInt(2);
                String dato2=res.getString(3);
                String dato3=res.getString(4);
                String dato4=res.getString(5);
                int dato5=res.getInt(6);
                String dato6=res.getString(7);
                String dato7=res.getString(8);
                int dato8=res.getInt(9);
                String dato9=res.getString(10);
                String dato10=res.getString(11);
                
                //Mostrar en pantalla
                this.jTextField1.setText(String.valueOf(dato1));
                this.jTextField2.setText(dato2);
                this.jTextField3.setText(dato3);
                this.jTextField4.setText(dato4);
                this.jTextField5.setText(String.valueOf(dato5));
                this.jTextField6.setText(String.valueOf(dato6));
                jTextField7.setText(dato7);
                jTextField8.setText(String.valueOf(dato8));
                jTextField9.setText(dato9);
                jTextField10.setText(dato10);
                
                this.CargarProdductos();
            }
        }catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al actualizar pantalla:\n" + ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        }
    }

