    public void Iniciar(){
        int noVenta=1;
            String SentenciaConsulta="select COUNT(NoProveedor) from Proveedor;";
            res=Conexiones.Conexion.Consulta(SentenciaConsulta);
            try{
                while(res.next())
                    noVenta=res.getInt(1);
            }catch(SQLException e){
                JOptionPane.showMessageDialog(null, "Error:\n" + e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            }
        if(noVenta!=0)
            CargarPantalla();
    }
    
