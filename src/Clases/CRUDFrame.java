    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        //Botón Aceptar, lee y comprueba los datos en los textField, de ser estos correctos
        //se desactiva y hace que los textField no sean editables
        switch(opcion){
            case 1: 
                    jButton6.setEnabled(false);
                    int noVenta=0;
                    String SentenciaConsulta="select MAX(NoProveedor) from Proveedor;";
                    res=Conexiones.Conexion.Consulta(SentenciaConsulta);
                    try{
                        while(res.next())
                            noVenta=res.getInt(1);
                    }catch(SQLException e){
                        JOptionPane.showMessageDialog(null, "Error:\n" + e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
                    }
                    
                    noVenta++;
                    
                    try {
                        estado=Conexiones.Conexion.InsertarProveedor(noVenta, jTextField2.getText(), jTextField3.getText(), jTextField4.getText(),
                                jTextField5.getText(), jTextField6.getText(), jTextField7.getText(), jTextField8.getText(),
                                jTextField9.getText(), jTextField10.getText());
                    } catch (SQLException ex) {}
                    if(estado){
                        contador=noVenta;
                        jTextField2.setEditable(false);
                        jTextField3.setEditable(false);
                        jTextField4.setEditable(false);
                        jTextField5.setEditable(false);
                        jTextField6.setEditable(false);
                        jTextField7.setEditable(false);
                        jTextField8.setEditable(false);
                        jTextField9.setEditable(false);
                        jTextField10.setEditable(false);
                        
                        CargarPantalla();
                        jButton3.setEnabled(true);
                        jButton4.setEnabled(true);
                        jButton5.setEnabled(true);
                    }
                    else
                        jButton6.setEnabled(true);
                    break;
            case 3:
                    jButton6.setEnabled(false);
                    try {
                        Conexiones.Conexion.ActualizarVenta(jTextField1.getText(),jTextField2.getText(),jTextField3.getText(),jTextField4.getText(),jTextField5.getText(),jTextField6.getText(),jTextField7.getText(),jTextField8.getText(),jTextField9.getText(),jTextField10.getText());
                    } catch (SQLException ex) {}
                    break;
        }
    }//GEN-LAST:event_jButton6ActionPerformed

